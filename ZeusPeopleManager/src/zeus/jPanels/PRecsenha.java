    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        String inputUserRecSenha = inputUserValidacao.getText();
        ConexaoDB conect_class = new ConexaoDB();
        Statement stmt = conect_class.iniciarConexao();
        try {
            ResultSet retorno_banco = stmt.executeQuery(
                    String.format("SELECT usuario, pgt_rec_senha FROM pessoa WHERE usuario = '%s';", inputUserRecSenha)
            );
            boolean laco = false;
            while (retorno_banco.next()){
                laco = true;
                if (!"".equals(retorno_banco.getString("usuario"))){
                    JOptionPane.showMessageDialog(null, "Recuperação de senha liberada!\nResponda o questionario.", "SUCESSO", JOptionPane.INFORMATION_MESSAGE);
                    comboboxPRecSenha.enable(true);
                    inputRRecSenha.enable(true);
                    btnAlterarSenha.enable(true);
                    novaSenha.enable(true);
                    //System.out.println("Recuperação de senha liberada!");
                    comboboxPRecSenha.setSelectedItem(String.format("%s", retorno_banco.getString("pgt_rec_senha")));
                    usuario_a_validar = inputUserRecSenha;
                } else {
                    JOptionPane.showMessageDialog(null, "Usuario ou e-mail não encontrado!", "ERRO", JOptionPane.ERROR_MESSAGE);
                }
            }
            if (laco == false){
                JOptionPane.showMessageDialog(null, "Usuario ou e-mail não encontrado!", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            System.out.println(String.format("Erro ao iniciar conexão: %s", ex));
        } 
        
    }//GEN-LAST:event_btnConfirmarActionPerformed

