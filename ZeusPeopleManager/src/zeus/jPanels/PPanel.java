    private void consultarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultarUsuarioActionPerformed
        int nivel_acesso = conexao.validarTipoPessoa(usuario);
        if (nivel_acesso == 1){
            Aluno aluno = conexao.extrairAluno(usuario);
            new PPesquUsuario(aluno).setVisible(true);
        } else if (nivel_acesso == 2){
            Professor professor = conexao.extrairProfessor(usuario);
            new PPesquUsuario(professor).setVisible(true);
        } else if (nivel_acesso == 3){
            Admin admin = conexao.extrairAdmin(usuario);
            new PPesquUsuario(admin).setVisible(true);
        }
    }//GEN-LAST:event_consultarUsuarioActionPerformed

