    private void btnAlterarSenhaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarSenhaActionPerformed
        if (usuario_a_validar != null){
            String resp_rec = inputRRecSenha.getText();
            String nova_senha = novaSenha.getText();
            ConexaoDB conect_class = new ConexaoDB();
            Statement stmt = null;
            Statement n_stmt = null;
            try {
                stmt = conect_class.iniciarConexao();
                ResultSet retorno_pregunta = stmt.executeQuery(String.format("SELECT resp_rec_senha FROM pessoa WHERE usuario = '%s' ;", usuario_a_validar));
                String resposta = conect_class.extrairString(retorno_pregunta, "resp_rec_senha");
                if (resp_rec.equals(resposta)){
                    n_stmt = conect_class.iniciarConexao();
                    String query = String.format("UPDATE pessoa SET senha = '%s' WHERE usuario = '%s';", nova_senha, usuario_a_validar);
                    n_stmt.executeUpdate(query);
                    JOptionPane.showMessageDialog(null, "Senha alterada com sucesso!", "Alteração de senha.", JOptionPane.INFORMATION_MESSAGE);
                    new PLogin().setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Resposta Incorreta!", "Alteração de senha.", JOptionPane.ERROR_MESSAGE);
                }
                
            } catch (SQLException ex) {
                System.out.println(String.format("Erro ao tentar redefinir a senha: ", ex));
            } 
        }
    }//GEN-LAST:event_btnAlterarSenhaActionPerformed

   
    

