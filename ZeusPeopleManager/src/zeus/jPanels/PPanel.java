    public String tipoUsuario(int nivel_de_acesso){
        if (nivel_de_acesso == 1){
            return "Aluno";
        } else if (nivel_de_acesso == 2){
            return "Professor";
        } else if (nivel_de_acesso == 3){
            return "Admin";
        }
        return "Desconhecido";
    }
    public void readJtable(){
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        Controler plist_result = new Controler();
        
        for (Pessoa p: plist_result.read()){
            modelo.addRow(new Object[]{
                p.getUsuario(),
                p.getNome(),
                tipoUsuario(p.getNivel_de_acesso()),
                p.getData_nascimento(),
                p.getEmail(),
                p.getTelefone()
            });
        }        
    }
    private void limparTabela() { 
        DefaultTableModel dm = null;
        while (jTable1.getRowCount () > 0) { 
            dm = (DefaultTableModel) jTable1.getModel ();
            dm.getDataVector ().removeAllElements ();
        }
    }
